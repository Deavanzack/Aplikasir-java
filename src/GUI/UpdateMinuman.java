    private void loadData() {
        try {
            Database db = new Database();
            String query = "SELECT * FROM minuman WHERE id = " + id;
            ResultSet rs = db.getData(query);
            if (rs.next()) {
                editNama.setText(rs.getString("nama"));
                editHarga.setValue(rs.getInt("harga"));
                editJumlah.setValue(rs.getInt("jumlah"));
                editKadaluarsa.setDate(rs.getDate("kadaluarsa"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateMinuman.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Gagal memuat data: " + ex.getMessage(), "Load Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        try {
            String nama = editNama.getText();
            int harga = (int) editHarga.getValue(); // pastikan ini mendapatkan int dengan benar, tergantung implementasi spinner
            int jumlah = (int) editJumlah.getValue(); // sama seperti harga
            java.util.Date utilDate = editKadaluarsa.getDate();
            java.sql.Date tgl = new java.sql.Date(utilDate.getTime());
            String status = "Belum";

            Database db = new Database(); // Pastikan database sudah diinstansiasi atau gunakan singleton jika sudah ada
            Minuman minuman = new Minuman(id, nama, harga, jumlah, tgl, status);
            minuman.Expired();
            db.updateMinuman(minuman.getId(), minuman.getNama(), minuman.getHarga(), minuman.getJumlah(), minuman.getTglKadaluarsa(), minuman.getStatus());
            
            javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
        } catch (SQLException ex) {
            Logger.getLogger(InputMainan.class.getName()).log(Level.SEVERE, null, ex);
            javax.swing.JOptionPane.showMessageDialog(null, "Gagal mengupdate data: " + ex.getMessage(), "Update Error", JOptionPane.ERROR_MESSAGE);
        }
        this.setVisible(false);
    }//GEN-LAST:event_buttonEditActionPerformed

